void IMU660RA_CS(imu* imu660ra,uint8_t x)
{
	(x) ? (HAL_GPIO_WritePin(imu660ra->GPIOx_CS, imu660ra->Pin_CS,GPIO_PIN_SET)) : (HAL_GPIO_WritePin(imu660ra->GPIOx_CS, imu660ra->Pin_CS,GPIO_PIN_RESET));
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief    IMU660RA 写寄存器
//  @param      cmd     寄存器地址
//  @param      val     需要写入的数据
//  @return     void
//  @since      v1.0
//  Sample usage:
//-------------------------------------------------------------------------------------------------------------------
void imu660ra_write_register(imu* imu660ra,uint8_t cmd, uint8_t val)
{
    uint8_t dat[2];
	
		imu660ra->IMU660RA_CS(imu660ra,0);


    dat[0] = cmd | IMU660RA_SPI_W;
    dat[1] = val;
    HAL_SPI_Transmit(imu660ra->hspi,dat,1,500);
		HAL_SPI_Transmit(imu660ra->hspi,&dat[1],1,500);

    imu660ra->IMU660RA_CS(imu660ra,1);
}

//-------------------------------------------------------------------------------------------------------------------
//  @brief    IMU660RA 写数据
//  @param      cmd     寄存器地址
//  @param      data     需要写入的数据
//  @return     void
//  @since      v1.0
//  Sample usage:
//-------------------------------------------------------------------------------------------------------------------
void imu660ra_write_registers(imu* imu660ra,uint8_t cmd, uint8_t* data, uint32_t length)
{
		uint8_t dat;
    imu660ra->IMU660RA_CS(imu660ra,0);
		dat = cmd | IMU660RA_SPI_W;
    HAL_SPI_Transmit(imu660ra->hspi,&dat,1,5000);
		HAL_SPI_Transmit(imu660ra->hspi,data,length,5000);
		

    imu660ra->IMU660RA_CS(imu660ra,1);
}

