    imu660ra->read_imu.imu_gyro_x = (int16_t)(((uint16_t)buf.dat[2]<<8 | buf.dat[1]));
    imu660ra->read_imu.imu_gyro_y = (int16_t)(((uint16_t)buf.dat[4]<<8 | buf.dat[3]));
    imu660ra->read_imu.imu_gyro_z = (int16_t)(((uint16_t)buf.dat[6]<<8 | buf.dat[5]));
		
